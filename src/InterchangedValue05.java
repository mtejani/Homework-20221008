public class InterchangedValue05
{

    public static void main(String[] args)
    {

        int a, b, temp;
        a = 50;
        b = 80;
        System.out.println("Before swapping : a, b = "+a+", "+ + b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("After swapping : a, b = "+a+", "+ + b);
    }


}
